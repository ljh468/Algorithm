public class 없는숫자더하기 {
    /********************************************************************************************************
     * 내 풀이
     ********************************************************************************************************/
    public int solution(int[] numbers) {
        int answer = 45;
        for(int num : numbers){
            answer -= num;
        }
        return answer;
    }
    /********************************************************************************************************
     * main
     ********************************************************************************************************/
    public static void main(String[] args) {
        없는숫자더하기 sol = new 없는숫자더하기();
        int[] numbers = {5,8,4,0,6,7,9};
        System.out.println("solution(numbers) : " + sol.solution(numbers));
    }
}
